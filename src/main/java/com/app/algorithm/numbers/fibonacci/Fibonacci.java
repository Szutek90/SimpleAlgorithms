package com.app.algorithm.numbers.fibonacci;

/**
 * The Fibonacci interface provides a method for generating Fibonacci numbers.
 */
public interface Fibonacci {
    /**
     * Returns the Fibonacci number at the specified index.
     *
     * @param idx the index of the Fibonacci number to retrieve
     * @return the Fibonacci number at the specified index
     */
    int getSequence(int idx);
}
